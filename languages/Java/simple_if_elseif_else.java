class simple_if_elseif_else{
public static void main(String arg[]){
    int a = 10;
    int b = 20;
    if(a>b)
    {
       System.out.println("A is greater.");
    }
    else if(b>a)
    {
        System.out.println("B is greater.");
    }
    else
    {
        System.out.println("They are equal.");
    }
   }
}